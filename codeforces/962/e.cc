static constexpr size_t MOD = 1e9 + 7;

void solve() {
  string s;
  cin >> s;

  u32 n = s.size();
  vec<i64> pref10(n + 1, 0);
  for (u32 i = 1; i <= n; ++i) {
    pref10[i] = pref10[i - 1] + (s[i - 1] == '1' ? 1 : -1);
  }
  map<i64, u64> cnt;
  cnt[0] = 1;

  u64 ans = 0;

  for (u32 y = 1; y <= n; ++y) {
    ans = (ans + cnt[pref10[y]] * (n - y + 1)) % MOD;
    cnt[pref10[y]] = (cnt[pref10[y]] + y + 1) % MOD;
  }

  println("{}", ans);
}

