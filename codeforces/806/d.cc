void solve() {
  u32 n;
  cin >> n;

  vec<string> strings(n);
  for (auto& s : strings)
    cin >> s;

  set<string> S;
  for (auto& s : strings) {
    S.insert(s);
  }

  for (auto& s : strings) {
    bool ok = false;

    for (u32 i = 0; s.size() > 1 && i < s.size() - 1; ++i) {
      if (S.contains(s.substr(0, i + 1)) && S.contains(s.substr(i + 1))) {
        ok = true;
        break;
      }
    }

    print("{}", ok ? '1' : '0');
  }

  println();
}

