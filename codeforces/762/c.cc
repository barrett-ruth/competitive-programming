void solve() {
  u64 a, s;
  cin >> a >> s;

  string ans;

  while (s) {
    auto ad = a % 10, sd = s % 10;
    if (ad <= sd) {
      ans += to_string(sd - ad);
    } else if (ad > sd && (10 + sd - ad) + ad == s % 100) {
      ans += to_string(10 + sd - ad);
      s /= 10;
    } else {
      println("-1");
      return;
    }
    s /= 10;
    a /= 10;
  }

  if (a) {
    println("-1");
    return;
  }

  reverse(all(ans));
  u32 i = 0;
  while (ans[i] == '0')
    ++i;
  for (; i < ans.size(); ++i)
    print("{}", ans[i]);
  println();
}

